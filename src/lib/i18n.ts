type DeepMutable<T> = T extends string ? string : T extends readonly (infer U)[] ? DeepMutable<U>[] : T extends (infer U)[] ? DeepMutable<U>[] : { -readonly [K in keyof T]: DeepMutable<T[K]> };
export type Translations = DeepMutable<typeof translations.en>;

